							<p><strong>FAQ</strong></p>
<p><strong>&iquest;C&oacute;mo puedo cambiar la informaci&oacute;n que especifique en el registro de IVAO?</strong></p>
<p>Si desea cambiar su nombre, Divisi&oacute;n o Direcci&oacute;n de correo, necesitara enviar un correo a <a href="mailto:md@ivao.aero">md@ivao.aero</a> en ingles solicitando el cambio.</p>
<p>Si desea cambiar otra informaci&oacute;n puede ingresar a <a href="http://www.ivao.aero/members/person/details.asp?ID=XXXXXX">http://www.ivao.aero/members/person/details.asp?ID=XXXXXX</a> (Remplace las XXXXXX por su VID) Y&nbsp; despu&eacute;s de click en &ldquo;Change My Details&rdquo;.</p>
<p><strong>Si estoy ocupado y no puedo conectarme a la red, &iquest;cu&aacute;nto tardara mi cuenta para cambiar a estado Inactivo?</strong></p>
<p>Si no se conecta en un periodo de 3 meses, su estado de cuenta va a cambiar a inactivo. Si no se conecta por 12 meses, su cuenta ser&aacute; eliminada.</p>
<p><strong>Si mi cuenta esta Inactiva, &iquest;C&oacute;mo la puedo reactivar?</strong></p>
<p>Debe ingresar a la p&aacute;gina de miembros (<a href="http://www.ivao.aero/members/person/details.asp">http://www.ivao.aero/members/person/details.asp</a>) donde aparecer&aacute; una viso diciendo &ldquo;Your Account is currently inactive on IVAO. To reactive it, click here&rdquo;. Cuando le haya dado click, su cuenta estar&aacute; activa nuevamente.</p>
<p><strong>&iquest;Puedo cambiar mi contrase&ntilde;a a algo que pueda recordar f&aacute;cilmente?</strong></p>
<p>Por motivos de seguridad, por ahora no, debe usar las contrase&ntilde;as generadas por IVAO.</p>
<p><strong>Si se me ha perdido la contrase&ntilde;a, &iquest;d&oacute;nde la puedo encontrar?</strong></p>
<p>Visite el sitio de recuperaci&oacute;n de contrase&ntilde;as y siga las instrucciones en pantalla. Podr&aacute; generar una nueva contrase&ntilde;a r&aacute;pidamente. (<a href="http://www.ivao.aero/members/person/password.htm">http://www.ivao.aero/members/person/password.htm</a>)</p>
<p><strong>Si me he creado dos cuentas en IVAO y quiero dejar solo una y poner mis horas en la que voy a utilizar, &iquest;Es posible?</strong></p>
<p>S&iacute;, es posible. Solo debe enviar sus dos VID&acute;s al staff de membres&iacute;a (<a href="http://www.ivaocol.co/new/www.ivao.aero/members">www.ivao.aero/members</a>) quienes se encargaran de este tema. Ellos eliminaran la cuenta que usted desee y a&ntilde;adir&aacute;n sus horas a la que quedara activa.</p>
<p><strong>Mis horas de vuelo no han sido asignadas aun, &iquest;es normal?</strong></p>
<p>Las horas son actualizadas cada d&iacute;a a las 1200Z (7am HLC). As&iacute; que si no han sido contadas, debe esperar al siguiente d&iacute;a.</p>
<p><strong>He visto gente con medallas y reconocimientos, &iquest;C&oacute;mo las obtengo?</strong></p>
<p>Visite la p&aacute;gina de IVAO Main Awards (http://www.ivao.aero/members/person/awards.asp)</p>
<p>Visite la p&aacute;gina de Division Awards (<a href="http://www.ivao.aero/members/divisions/default.asp">http://www.ivao.aero/members/divisions/default.asp</a>)</p>
<p><strong>&iquest;C&oacute;mo s&eacute; qu&eacute; tipo de Staff requiero para solucionar mi problema?</strong></p>
<p>Visite la secci&oacute;n de Staff de esta p&aacute;gina para ver las responsabilidades de cada uno.</p>

