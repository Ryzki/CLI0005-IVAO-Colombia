<p>Estas siempre ser&aacute;n entre el nivel medio del mar y un punto en el aire.</p>

<ul>
	<li>Altitud de transici&oacute;n: En Colombia siempre ser&aacute; 18000ft y por debajo de esta se ubicaran las altitudes, expresadas en miles Ej. A12000 (Uno Dos mil).</li>
	<li>Nivel de transici&oacute;n: En Colombia siempre ser&aacute; 190 y por encima de esta se ubican los Niveles de Vuelo, expresados en cientos de miles Ej. F270 (Dos Siete Cero).</li>
