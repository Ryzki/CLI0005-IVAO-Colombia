<p><strong>Some Information</strong></p>

<p>La republica de Colombia, se encuentra ubicada al noroeste de Suram&eacute;rica, siendo el &uacute;nico pa&iacute;s ba&ntilde;ado por dos oc&eacute;anos en este continente, el Atl&aacute;ntico y el Pacifico, posee una superficie de 1.141.748 km&sup2;, que sumada a la extensi&oacute;n mar&iacute;tima arroja un total de 2.070.408 km&sup2;, y tiene una poblaci&oacute;n cercana a los 45 millones de habitantes.</p>

<p>El pa&iacute;s se caracteriza por ser en su mayor&iacute;a monta&ntilde;oso, debido a el sistema de cordilleras ubicado en el suroccidente, centro, occidente y nororiente, por el contrario las llanuras se encuentran en el oriente y el norte y el sur caracteriz&aacute;ndose este ultimo por ser un lugar selv&aacute;tico y de dif&iacute;cil acceso siendo el transporte a&eacute;reo al &uacute;nica herramienta para llegar all&iacute;.</p>

<p>Colombia es un pa&iacute;s que debido a su geograf&iacute;a no tiene conectados v&iacute;a terrestre la totalidad de sus departamentos, siendo el &uacute;nico medio de transporte &uacute;til el avi&oaoacute;n, por esto se poseen aer&oacute;dromos de distintas caracter&iacute;sticas como lo son, en medio de la selva, pistas cortas, procedimientos complicados, pero tambi&eacute;n aeropuertos de gran calibre como Bogot&aacute;, Rionegro y otros mas.</p>

